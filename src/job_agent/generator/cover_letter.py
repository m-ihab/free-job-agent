"""Generate a cover letter (as Markdown) tailored to a job listing."""
from __future__ import annotations

from rapidfuzz import fuzz

from job_agent.schemas.candidate import CandidateProfile, MasterCV
from job_agent.schemas.job import JobListing


def _relevance_score(exp_technologies: list[str], job: JobListing) -> float:
    """Score a work experience against a job."""
    job_lower = [t.lower() for t in job.tech_stack]
    exp_lower = [t.lower() for t in exp_technologies]
    if not job_lower:
        return 0.5
    matched = sum(
        1
        for jt in job_lower
        for et in exp_lower
        if fuzz.ratio(jt, et) >= 80
    )
    return matched / len(job_lower)


def generate_cover_letter(
    job: JobListing, master_cv: MasterCV, profile: CandidateProfile
) -> str:
    """Generate a tailored cover letter in Markdown. Only uses facts from master_cv/profile."""
    lines: list[str] = []
    lines.append(
        "<!-- Generated by free-job-agent. All facts sourced from profile. -->"
    )
    lines.append("")

    name = master_cv.contact.name
    email = master_cv.contact.email

    lines.append(f"# Cover Letter — {job.title} at {job.company}")
    lines.append("")
    lines.append(f"**{name}**  ")
    lines.append(f"{email}  ")
    if master_cv.contact.phone:
        lines.append(f"{master_cv.contact.phone}  ")
    lines.append("")

    # Opening paragraph
    lines.append(
        f"Dear Hiring Manager at {job.company},\n"
    )
    lines.append(
        f"I am writing to express my strong interest in the **{job.title}** position at "
        f"**{job.company}**. {master_cv.summary}"
    )
    lines.append("")

    # Middle paragraph — top 2 most relevant experiences
    scored = sorted(
        master_cv.experience,
        key=lambda e: _relevance_score(e.technologies, job),
        reverse=True,
    )
    top_exps = scored[:2]
    if top_exps:
        exp_lines = []
        for exp in top_exps:
            if exp.bullet_points:
                highlight = exp.bullet_points[0]
                exp_lines.append(
                    f"In my role as **{exp.title}** at **{exp.company}**, {highlight.lower()}"
                )
        lines.append(" ".join(exp_lines))
        lines.append("")

    # Closing paragraph
    auth_note = ""
    if profile.work_authorizations:
        auth_note = (
            f" I am {profile.work_authorizations[0]} and do not require visa sponsorship."
        )
    lines.append(
        f"I am excited about the opportunity to contribute to {job.company} and would welcome "
        f"the chance to discuss how my background aligns with your team's needs.{auth_note} "
        f"Thank you for considering my application."
    )
    lines.append("")
    lines.append(f"Sincerely,  ")
    lines.append(f"**{name}**")
    lines.append("")

    return "\n".join(lines)
