def test_answer_screening_questions_matched(sample_qa_profile, sample_profile):
    questions = ["Are you authorized to work in France?"]
    answers = answer_screening_questions(questions, sample_qa_profile, sample_profile)
    assert "Are you authorized to work in France?" in answers
    answer = answers["Are you authorized to work in France?"]
    assert answer != MANUAL_REVIEW_MARKER
    assert "authorized" in answer or "authorized" in answer.lower()


def test_answer_screening_questions_unmatched(sample_qa_profile, sample_profile):
    questions = ["What is your favorite color?"]
    answers = answer_screening_questions(questions, sample_qa_profile, sample_profile)
    assert answers["What is your favorite color?"] == MANUAL_REVIEW_MARKER


def test_answer_screening_questions_mixed(sample_qa_profile, sample_profile):
    questions = [
        "Do you require visa sponsorship?",
        "What is your hobby?",
    ]
    answers = answer_screening_questions(questions, sample_qa_profile, sample_profile)
    assert answers["Do you require visa sponsorship?"] != MANUAL_REVIEW_MARKER
    assert answers["What is your hobby?"] == MANUAL_REVIEW_MARKER


def test_answer_screening_empty_questions(sample_qa_profile, sample_profile):
    answers = answer_screening_questions([], sample_qa_profile, sample_profile)
    assert answers == {}
