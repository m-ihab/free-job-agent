"""Tests for the generator modules."""

from job_agent.generator.cover_letter import generate_cover_letter
from job_agent.generator.cv import tailor_cv
from job_agent.generator.qa import answer_screening_questions, MANUAL_REVIEW_MARKER


def test_tailor_cv_returns_markdown(sample_job, sample_master_cv, sample_profile):
    result = tailor_cv(sample_job, sample_master_cv, sample_profile)
    assert isinstance(result, str)
    assert len(result) > 0


def test_tailor_cv_contains_candidate_name(sample_job, sample_master_cv, sample_profile):
    result = tailor_cv(sample_job, sample_master_cv, sample_profile)
    assert "Candidate Data AI Paris" in result


def test_tailor_cv_contains_source_comment(sample_job, sample_master_cv, sample_profile):
    result = tailor_cv(sample_job, sample_master_cv, sample_profile)
    assert "Generated by free-job-agent" in result


def test_tailor_cv_has_experience_section(sample_job, sample_master_cv, sample_profile):
    result = tailor_cv(sample_job, sample_master_cv, sample_profile)
    assert "## Experience" in result


def test_tailor_cv_has_skills_section(sample_job, sample_master_cv, sample_profile):
    result = tailor_cv(sample_job, sample_master_cv, sample_profile)
    assert "## Skills" in result


